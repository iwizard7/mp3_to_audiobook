#!/bin/bash

echo "Сборка приложения MP3ToAudiobook..."

# Сборка приложения
swift build

if [ $? -eq 0 ]; then
    echo "Сборка завершена успешно!"
    echo "Запуск приложения..."

    # Запуск приложения в фоне
    swift run &

    # Ожидание запуска
    sleep 2

    echo "Приложение запущено!"
    echo "Если окно не появилось, проверьте Dock или используйте Activity Monitor"
    echo "Для остановки приложения используйте Ctrl+C или закройте окно"

    # Ожидание завершения
    wait
else
    echo "Ошибка сборки!"
    exit 1
fi